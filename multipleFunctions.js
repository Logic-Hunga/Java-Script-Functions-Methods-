/**Defining the functions */

// Subtraction function(method)
function subtractionFunction(number1, number2, number3) {
    console.log(number1 - number2 - number3);
}
// Addition function(method)
function AdditionFunction(number1, number2, number3) {
    console.log(number1 + number2 + number3);
}
// Multiplication function(method)
function Multiplication(number1, number2, number3) {
    console.log(number1 * number2 * number3);
}

// invoking the functions
console.log("The  is : ");
subtractionFunction(20, 6, 1); // subtraction function
console.log("The sum of numbers is ")
AdditionFunction(3, 4, 9); // Addition function
console.log("The product of numbers is: ");
Multiplication(2, 5, 20);

